public class DataTypesDemo {
    public static void main(String[] args) {
        String  str        = "Audintel";
        int     intValue   = 10;
        float   floatValue = 10.0f;
        double  dblValue   = 10.0;
        long    longValue  = 10l;
        boolean flag       = true;
        short   sint       = 10;
        System.out.println("Hello world!"+str);
        System.out.println("Hello world!"+intValue);
        System.out.println("Hello world!"+floatValue);
        System.out.println("Hello world!"+dblValue);
        System.out.println("Hello world!"+longValue);
        System.out.println("Hello world!"+flag);
        System.out.println("Hello world!"+sint);
    }
}